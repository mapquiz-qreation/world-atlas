const fs = require('fs');
const path = require('path');

function load(name) {
  return JSON.parse(fs.readFileSync(`./data/${name}.json`, 'utf8'));
}
function save(name, data) {
  fs.writeFileSync(`./data/${name}.json`, JSON.stringify(data, null, 2), 'utf8');
  console.log(`✅ ${name}.json saved`);
}

