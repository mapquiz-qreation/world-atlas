const fs = require('fs');
const data = JSON.parse(fs.readFileSync('./data/timeline.json', 'utf8'));

// ── ヘルパー ────────────────────────────────────────────────
function setEmpires(label, empires) {
  const epoch = data.find(e => e.label === label);
  if (!epoch) { console.error('NOT FOUND:', label); return; }
  epoch.empires = empires;
  console.log('Set empires for:', label, '/', empires.length, 'empires');
}

