const DATA_FILES = [
  'europe','mideast','africa','india','china',
  'southeast_asia','north_america','latin_america','world_wars','cold_war'
];

let patched = 0;
DATA_FILES.forEach(f => {
  const path = `data/${f}.json`;
  const data = JSON.parse(fs.readFileSync(path, 'utf8'));
  let changed = false;

  Object.values(data.eras).forEach(era => {
    era.fixed.forEach(q => {
      if (q.explanation) return; // 既に解説があればスキップ

      const patch = PATCHES.find(p => q.text.includes(p.match));
      if (!patch) return;

      q.explanation = patch.explanation;
      if (patch.relatedTerms && !q.relatedTerms?.length) {
        q.relatedTerms = patch.relatedTerms;
      }
      if (patch.syncro && !q.syncro) {
        q.syncro = patch.syncro;
      }
      patched++;
      changed = true;
      console.log(`  ✅ ${q.text.slice(0, 40)}`);
    });
  });

  if (changed) {
    fs.writeFileSync(path, JSON.stringify(data, null, 2), 'utf8');
    console.log(`[${f}] 保存完了`);
  }
});

console.log(`\n完了: ${patched}問にパッチ適用`);
