// ─── 適用 ─────────────────────────────────────────────────────────────────────
let added = 0;
for (const epoch of timeline) {
  const key = String(epoch.year);
  const ev = japanEvents[key];
  if (!ev) continue;
  const dup = epoch.events.some(e => e.title === ev.title);
  if (dup) {
    console.log(`⏭ [${key}] "${ev.title}" は既に存在 → スキップ`);
    continue;
  }
  epoch.events.push(ev);
  added++;
  console.log(`✅ [${key}] ${ev.title}`);
}

writeFileSync(PATH, JSON.stringify(timeline, null, 2), 'utf8');
console.log(`\n💾 ${added} 件の japan イベントを追加`);

// サマリー
const allJapan = timeline.flatMap(e => e.events).filter(e => e.region === 'japan');
console.log(`📊 japan 総イベント数: ${allJapan.length}`);
console.log(`📊 japan が存在するエポック数: ${timeline.filter(ep => ep.events.some(ev => ev.region === 'japan')).length} / ${timeline.length}`);
console.log(`📊 全イベント数: ${timeline.reduce((s, e) => s + e.events.length, 0)}`);
