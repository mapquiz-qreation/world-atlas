function addToFile(filename, additions) {
  const data = load(filename);
  let totalAdded = 0;
  for (const [eraKey, questions] of Object.entries(additions)) {
    if (!data.eras[eraKey]) {
      console.warn(`  ⚠️  ${filename}: era "${eraKey}" が見つかりません`);
      continue;
    }
    if (!data.eras[eraKey].fixed) {
      data.eras[eraKey].fixed = [];
    }
    data.eras[eraKey].fixed.push(...questions);
    totalAdded += questions.length;
    console.log(`  ${eraKey}: +${questions.length}問 追加`);
  }
  save(filename, data);
  console.log(`  合計 +${totalAdded}問 追加\n`);
  return totalAdded;
}

