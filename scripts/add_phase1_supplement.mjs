import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DATA = join(__dirname, '..', 'data');

function load(file) {
  return JSON.parse(readFileSync(join(DATA, file), 'utf8'));
}
function save(file, data) {
  writeFileSync(join(DATA, file), JSON.stringify(data, null, 2), 'utf8');
}
function addToEra(data, eraKey, questions) {
  if (!data.eras[eraKey]) return 0;
  if (!data.eras[eraKey].fixed) data.eras[eraKey].fixed = [];
  data.eras[eraKey].fixed.push(...questions);
  return questions.length;
}

