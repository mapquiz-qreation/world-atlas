let updated = 0;
let skipped = 0;

for (const era of Object.values(europe.eras)) {
  if (!era.fixed) continue;
  for (const q of era.fixed) {
    if (q.lat === undefined || q.lng === undefined) {
      // エリア問題はスキップ
      skipped++;
      continue;
    }
    if (q.advancedText) {
      // 既にあるものはスキップ
      continue;
    }
    const adv = advancedMap[q.text];
    if (adv) {
      q.advancedText = adv;
      updated++;
    }
  }
}

writeFileSync(join(DATA, 'europe.json'), JSON.stringify(europe, null, 2), 'utf8');
console.log(`✅ europe.json advancedText追記完了`);
console.log(`  更新: ${updated}問 / エリア問題スキップ: ${skipped}問`);

// カバレッジ確認
let hasAdv = 0, noAdv = 0;
const noAdvList = [];
for (const [eraKey, era] of Object.entries(europe.eras)) {
  if (!era.fixed) continue;
  for (const q of era.fixed) {
    if (q.lat === undefined) continue;
    if (q.advancedText) hasAdv++;
    else { noAdv++; noAdvList.push(`${eraKey}: ${q.text}`); }
  }
}
console.log(`\nadvancedTextカバレッジ: ${hasAdv}/${hasAdv+noAdv}問 (${Math.round(hasAdv/(hasAdv+noAdv)*100)}%)`);
if (noAdvList.length > 0 && noAdvList.length <= 30) {
  console.log('\nadvancedText未設定の問題:');
  noAdvList.forEach(t => console.log(' - ' + t));
}
